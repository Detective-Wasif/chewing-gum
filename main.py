import sys
import itertools

spaces = {1: ' ', 2: 'e', 3: '"', 4: 'a', 5: 's', 6: 'i', 7: 'n', 8: 'r', 9: '\n', 10: 'l', 11: 'o', 12: 'p', 13: 'm', 14: ')', 15: '(', 16: 'd', 17: 'f', 18: 'c', 19: '_', 20: '=', 21: 'u', 22: ':', 23: '.', 24: 't', 25: ',', 26: '0', 27: 'b', 28: 'h', 29: 'v', 30: 'g', 31: 'y', 32: 'k', 33: '1', 34: '[', 35: ']', 36: '#', 37: 'x', 38: '+', 39: '2', 40: 'N', 41: 'E', 42: 'V', 43: '\\', 44: 'w', 45: 'C', 46: '3', 47: '8', 48: '9', 49: '5', 50: '-', 51: "'", 52: '6', 53: '7', 54: '4', 55: 'Y', 56: 'A', 57: 'T', 58: 'L', 59: 'I', 60: 'G', 61: 'z', 62: 'S', 63: 'F', 64: 'j', 65: 'Q', 66: 'P', 67: 'R', 68: '/', 69: 'O', 70: 'M', 71: '}', 72: '*', 73: '{', 74: 'W', 75: 'U', 76: 'q', 77: 'B', 78: '>', 79: '<', 80: 'D', 81: '!', 82: 'H', 83: '%', 84: 'X', 85: 'K', 86: ';', 87: '|', 88: '^', 89: '?', 90: '&', 91: '`', 92: 'J', 93: '~', 94: '@', 95: '$', 96: 'Z', 97: '\x0c', 98: '\x0b', 99: '\r', 100: '\t'}

file = sys.argv[1]
program = open(file).read()
final = ''

for _, token in itertools.groupby(program):
    token = list(token)
    if all(char.isspace() for char in token):
        token = spaces[len(token)]
        final += token
    else:
        token = "".join(token)
        final += token

final = final[:-1]
exec(final)
